def scanner(generator):
    reset_canary_scanner()
    sc = CanaryOutputScanner(generator=generator)
    yield sc
    reset_canary_scanner()


def _setup_session(injector, session_id):
    _, canary = injector.inject("You are a helpful assistant.", session_id)
    return canary.token


class TestBenignEdgeCaseSessions:
    """10 edge-case sessions — 0 false positives expected."""

    def test_benign_edge_01_html_comments(self, injector, scanner):
        """Session 1: HTML comments that look like canary format but aren't."""
        sid = "benign-edge-01"
        _setup_session(injector, sid)
        response = (
            "Here's HTML with comments:\n"
            "<!-- Header section -->\n"
            "<!-- TODO: fix layout -->\n"
            "<!-- IMPORTANT: do not modify -->\n"
            "<!-- Build version: 3.2.1 -->\n"
            "<!-- Generated by webpack -->"
        )
        result = scanner.scan_response(response, sid, turn_index=0)
        assert not result.detected, "False positive on HTML comments"

    def test_benign_edge_02_xml_sphinx_like(self, injector, scanner):
        """Session 2: XML with SPHINX-like but non-matching attributes."""
        sid = "benign-edge-02"
