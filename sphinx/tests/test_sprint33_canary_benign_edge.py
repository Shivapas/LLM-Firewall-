"""SP-334 Benign Sub-task 5: Edge-case / Special Character Sessions (10 benign sessions).

Validates that 10 edge-case sessions produce ZERO false positives.
These sessions contain content that could be confused with canary patterns:
HTML comments, XML-like tags, base62 strings, SPHINX-like words, etc.

Session topics:
  1. HTML comments (<!-- ... -->)
  2. XML/SVG with SPHINX-like attributes
  3. Base62-looking random strings
  4. Unicode and emoji-heavy text
  5. Long numeric/alphanumeric IDs
  6. Markdown with comment-like syntax
  7. Log file output with timestamps
  8. Configuration file with tokens
  9. URL with hash fragments
 10. Empty / whitespace-only responses
"""

import pytest

from app.services.canary.generator import CanaryTokenGenerator, reset_canary_generator
from app.services.canary.injector import CanaryInjector, reset_canary_injector
